# From (Select Floor(PRICE/10000)*10000 as PRICE_GROUP From PRODUCT) PRODUCT
# Group by PRICE_GROUP
# Order by PRICE_GROUP

SELECT 
    FLOOR(PRICE/10000)*10000 AS PRICE_GROUP, 
    COUNT(*) AS PRODUCTS
FROM PRODUCT
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP






















